# ---- Pydantic Response Schemas for Gemini ----

class DockerfileResult(BaseModel):
    """Structured Gemini response containing generated Dockerfile and docker-compose content."""
    dockerfile_content: str = Field(
        description="Complete Dockerfile content with multi-stage build, optimized layers, and security best practices."
    )
    compose_content: str = Field(
        description="Complete docker-compose.yml content exposing the application with health checks."
    )
    base_image: str = Field(
        default="python:3.12-slim",
        description="The base Docker image used in the Dockerfile."
    )
    exposed_port: int = Field(
        default=8000,
        description="The primary port the application exposes inside the container."
    )
    entrypoint_command: str = Field(
        default="",
        description="The CMD or ENTRYPOINT used to start the application."
    )
    optimization_notes: List[str] = Field(
        default_factory=list,
        description="List of optimization decisions made in the Dockerfile generation."
    )


class BuildResult(BaseModel):
    """Result of a Docker image build operation."""
    success: bool = Field(description="Whether the build completed successfully.")
    image_id: str = Field(default="", description="SHA256 image ID of the built image.")
    image_name: str = Field(default="", description="Full image name with tag.")
    image_tag: str = Field(default="latest", description="Image tag.")
    size_bytes: int = Field(default=0, description="Image size in bytes.")
    build_logs: List[str] = Field(default_factory=list, description="Build output log lines.")
    error_message: str = Field(default="", description="Error details if build failed.")


